#集合可以使用两种方式进行定义
#一种使用{,,,}
#一种使用set函数来定义
A={"python","hhh"}
print(A)
B={"py123",1,2,3,4,5}
print(B)
#A交B
C=A&B
print(C)
#A并B
C=A|B
print(C)
#A减去B
C=A-B
print(C)
#A与B的交集的补集
C=A^B
print(C)
#A集合是否包含B集合
C=A>=B
print(C)
A.add(5)
print(A)
#删除集合B中的元素1，如果不存在，则不删除
B.discard(1)
print(B)
#随机删除集合B中的一个元素，更新S，如果集合B为空，则抛出异常
B.pop()
print(B)
#清空集合B
B.clear()
print(B)
#删除集合A中的元素3，如果集合中不存在，则抛出异常
A.remove(3)
print(A)
